// 1. What is JavaScript?

// A simple message to show the purpose of JavaScript
console.log("JavaScript is a programming language used for creating interactive web pages.");

// 2. Basic Syntax Rules

// Case sensitivity - 'myVariable' and 'myvariable' are different
let myVariable = "Hello, JavaScript!";
let myvariable = "Goodbye, JavaScript!";

// Displaying both variables
console.log(myVariable);   // Output: Hello, JavaScript!
console.log(myvariable);   // Output: Goodbye, JavaScript!

// Semicolons - The semicolon is optional, but it's good practice to use it
let number = 10;  // Semicolon used here
let message = "Learning JavaScript is fun!"  // No semicolon here, still works

console.log(number);   // Output: 10
console.log(message);  // Output: Learning JavaScript is fun!

// 3. Writing Your First JavaScript Program

// Printing a message to the console
console.log("Hello, World!");  // Output: Hello, World!

// 4. Comments

// This is a single-line comment
console.log("This is a comment in JavaScript.");

// This is a multi-line comment
/*
  You can use multi-line comments to explain
  complex logic or leave notes for yourself.
*/
console.log("This is a multi-line comment example.");

// 5. Using Variables
let greeting = "Hello";
let name = "Siddh";
console.log(greeting + ", " + name + "!");  // Output: Hello, Siddh!

// 6. String Concatenation vs Template Literals

// String concatenation
let sentence1 = "JavaScript is " + "awesome!";
console.log(sentence1);  // Output: JavaScript is awesome!

// Using Template Literals
let sentence2 = `JavaScript is really awesome!`;
console.log(sentence2);  // Output: JavaScript is really awesome!

